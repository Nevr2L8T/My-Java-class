package Day_13_WhileLoop;

public class Topic5_DoWhileLoop {
    public static void main(String[] args) {
        int i=0;
        do {
            System.out.println("the i value: "+i);
            i++;

        }while (i<10);
        int j = 0;
        while (j<10){
            j++;
        }
    }
}
