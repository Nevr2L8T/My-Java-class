package Day_4_Operators;

public class PrePostConditions {
    public static void main(String[] args) {


        int num = 10;
        //num++;
        System.out.println(num++);
        System.out.println(num);
        num++;
        System.out.println(num);

        int num1 =10;
        System.out.println(++num1);

        //after--

        int num2 = 20;
        System.out.println(num2--);
        System.out.println(num2);

        int num3 = 15;
        System.out.println(--num3);



    }
}
